import 'package:flutter/material.dart';
import 'package:sayaaratukum/util/constant.dart';

EdgeInsetsGeometry paddingStart() {
  return const EdgeInsets.only(
    left: Constants.spacing16,
  );
}

EdgeInsetsGeometry padding16() {
  return const EdgeInsets.all(
    Constants.spacing16,
  );
}

EdgeInsetsGeometry spacingH16() {
  return const EdgeInsets.symmetric(
    horizontal: Constants.spacing16,
  );
}

EdgeInsetsGeometry spacing32() {
  return const EdgeInsets.all(
    Constants.spacing16,
  );
}
